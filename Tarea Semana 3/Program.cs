{
    int CAN, C200, C100, C50, C20, C10, C5;
    CAN = C200 = C100 = C50 = C20 = C10 = C5 = 0;
    string linea;

    decimal cantidad_moneda;// = 0.0d;



    Console.Write("Ingrese una cantidad: ");
    linea = Console.ReadLine();
    //CAN = Convert.ToInt32(linea);


    cantidad_moneda = Convert.ToDecimal(linea);
    //cantidad_moneda = (cantidad_moneda * 7.89m); //convierte de dolares a Quetzales



    CAN = Convert.ToInt32(Math.Truncate(cantidad_moneda));

    cantidad_moneda = (cantidad_moneda - CAN) * 100;
    Console.WriteLine($"Centavos = {cantidad_moneda / 100}");

    while (CAN >= 5)
    {

        switch (CAN)
        {
            case >= 200:
                {
                    C200 = (CAN / 200);
                    CAN = CAN - (C200 * 200);
                    break;
                }
            case >= 100:
                {
                    C100 = (CAN / 100);
                    CAN = CAN - (C100 * 100);
                    break;
                }
            case >= 50:
                {
                    C50 = (CAN / 50);
                    CAN = CAN - (C50 * 50);
                    break;
                }
            case >= 20:
                {
                    C20 = (CAN / 20);
                    CAN = CAN - (C20 * 20);
                    break;
                }

            case >= 10:
                {
                    C10 = (CAN / 10);
                    CAN = CAN - (C10 * 10);
                    break;
                }

            case >= 5:
                {
                    C5 = (CAN / 5);
                    CAN = CAN - (C5 * 5);
                    break;
                }
        }
    }
    //cantidades billetes:

    Console.WriteLine($"--- ESTOS SON TUS BILLETES ---");
    Console.WriteLine($"BILLETES DE 200: {C200}");
    Console.WriteLine($"BILLETES DE 100: {C100}");
    Console.WriteLine($"BILLETES DE 50: {C50}");
    Console.WriteLine($"BILLETES DE 20: {C20}");
    Console.WriteLine($"BILLETES DE 10: {C10}");
    Console.WriteLine($"BILLETES DE 5: {C5}");
    Console.WriteLine($"BILLETES DE 1: {CAN}");


    ////monedas
    int M, M50, M25, M10, M5, M1;
    M = M50 = M25 = M10 = M5 = M1 = 0;
    M = Convert.ToInt32(cantidad_moneda);

    while (M >= 5)
    {

        switch (M)
        {
            case >= 50:
                {
                    M50 = ((int)cantidad_moneda / 100);
                    M50 = (M / 50);
                    M = M - (M50 * 50);
                    break;
                }
            case >= 25:
                {
                    M25 = ((int)cantidad_moneda / 100);
                    M25 = (M / 25);
                    M = M - (M25 * 25);
                    break;
                }
            case >= 10:
                {
                    M10 = ((int)cantidad_moneda / 100);
                    M10 = (M / 10);
                    M = M - (M10 * 10);
                    break;
                }
            case >= 5:
                {
                    M5 = ((int)cantidad_moneda / 100);
                    M5 = (M / 5);
                    M = M - (M5 * 5);
                    break;
                }
            case >= 1:
                {
                    M1 = ((int)cantidad_moneda / 100);
                    M1 = (M / 1);
